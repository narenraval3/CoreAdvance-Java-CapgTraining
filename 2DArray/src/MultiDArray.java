
public class MultiDArray {

	public static void main(String[] args) {
		
		int y[][] = new int[2][2];
		y[0][0] = 100;
		y[0][1] = 200;
		y[1][0] = 300;
		y[1][1] = 400;
		
		System.out.println("1st val" + y[0][0]);
		System.out.println("2nd val" + y[0][1]);
		System.out.println("3rd val" + y[1][0]);
		System.out.println("4th val" + y[1][1]);
		
	}

}
